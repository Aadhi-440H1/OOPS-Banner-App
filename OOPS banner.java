public class BannerOOPS {
    public static void main(String[] args) {
        // Each line prints O O P S side by side
        System.out.println("  ***   " + "  ***   " + " *****  " + "  ***** ");
        System.out.println(" *   *  " + " *   *  " + " *    * " + " *      ");
        System.out.println(" *   *  " + " *   *  " + " *    * " + " *      ");
        System.out.println(" *   *  " + " *   *  " + " *****  " + "  ***** ");
        System.out.println(" *   *  " + " *   *  " + " *       " + "      * ");
        System.out.println(" *   *  " + " *   *  " + " *       " + "      * ");
        System.out.println("  ***   " + "  ***   " + " *       " + " *****  ");
    }
}